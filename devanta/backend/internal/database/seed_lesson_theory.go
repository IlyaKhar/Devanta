// Детальная теория под каждый из 30 уроков (до итога) — разный текст, не только общий абзац блока.
func lessonTheoryDetail(track string, sortOrder int) string {
	if sortOrder < 1 || sortOrder > 30 {
		return ""
	}
	idx := sortOrder - 1
	base := theoryLessonBodies[idx] + "\n\n" + theoryLessonDeepDive[idx]
	switch track {
	case "py":
		return base + "\n\nСвязь с Python: те же идеи в синтаксисе `def`, отступов и `print()` — смотри параллели в конспекте."
	case "web":
		return base + "\n\nСвязь с вебом: подумай, где в странице (HTML/CSS/скрипт) это проявляется: структура, стили, обработка в браузере."
	default:
		return base
	}
}

